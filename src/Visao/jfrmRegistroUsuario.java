    public void preencheUsuario(String sql) {
        try {
            con.excutaSQL(sql);
            con.rs.first();
            jLabelCod.setText("" + con.rs.getInt("id_usuario"));
            jTextFieldNome.setText(con.rs.getString("nome_usuario"));
            jTextFieldCpf.setText(con.rs.getString("cpf"));
            jTextFieldRg.setText(con.rs.getString("rg"));
            jTextFieldNis.setText(con.rs.getString("nis"));
            String sexo = con.rs.getString("sexo");
            if (sexo.equals("MASCULINO")) {
                jRadioButtonMasculino.setSelected(true);
            } else {
                jRadioButtonFeminino.setSelected(true);
            }
            
            String data = con.rs.getString("data_nascimento");
            try {
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(data);
                jDateChooserDataNascimento.setDate(date);
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }

            String sabe_ler = con.rs.getString("sabe_ler");
            if (sabe_ler.equals("SIM")) {
                jRadioButtonSabeSim.setSelected(true);
            } else {
                jRadioButtonSabeNao.setSelected(true);
            }
            String frequenta_escola = con.rs.getString("frequenta_escola");
            if (frequenta_escola.equals("SIM")) {
                jRadioButtonFrequentaSim.setSelected(true);
            } else {
                jRadioButtonFrequentaNao.setSelected(true);
            }
            String escolaridade = con.rs.getString("escolaridade");
            jComboBoxEscolaridade.setSelectedItem(escolaridade);
            String possui_ctps = con.rs.getString("possui_ctps");
            if (possui_ctps.equals("SIM")) {
                jRadioButtonPossuiSim.setSelected(true);
            } else {
                jRadioButtonPossuiNao.setSelected(true);
            }
            String condicao_ocupacao = con.rs.getString("condicao_ocupacao");
            jComboBoxCondicao.setSelectedItem(condicao_ocupacao);
            jTextFieldQualificao.setText(con.rs.getString("qualificacao_profissional"));
            jTextFieldRendaMensal.setText(String.valueOf(con.rs.getDouble("renda_mensal")));
            jTextFieldBairro.setText(con.rs.getString("bairro"));
            jTextFieldEndereco.setText(con.rs.getString("endereco"));
            jTextFieldNumero.setText(con.rs.getString("numero"));

        } catch (SQLException ex) {
            Logger.getLogger(jfrmRegistroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

