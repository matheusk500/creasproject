    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        if (testarNumero(jTextFieldRendaMensal.getText()) == true && !jTextFieldNome.getText().trim().isEmpty() && jDateChooserDataNascimento.getDate() != null) {
            ModeloUsuario modUsuario = new ModeloUsuario();
            modUsuario.setNome_usuario(jTextFieldNome.getText().toUpperCase());
            modUsuario.setCpf(jTextFieldCpf.getText().toUpperCase());
            modUsuario.setRg(jTextFieldRg.getText().toUpperCase());
            modUsuario.setNis(jTextFieldNis.getText().toUpperCase());
            modUsuario.setBairro(jTextFieldBairro.getText().toUpperCase());
            modUsuario.setEndereco(jTextFieldEndereco.getText().toUpperCase());
            modUsuario.setNumero(jTextFieldNumero.getText().toUpperCase());
            
            if (jRadioButtonMasculino.isSelected()) {
                modUsuario.setSexo("MASCULINO");
            } else {
            }

            if (jRadioButtonFeminino.isSelected()) {
                modUsuario.setSexo("FEMININO");
            }

            if (jDateChooserDataNascimento.getDate() == null) {
                modUsuario.setData_nascimento("01/01/0001");
            } else {
                java.util.Date pega = jDateChooserDataNascimento.getDate();
                SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
                String data = formato.format(pega);
                modUsuario.setData_nascimento(data);
            }

            if (jRadioButtonSabeSim.isSelected()) {
                modUsuario.setSabe_ler("SIM");
            } else {
            }
            if (jRadioButtonSabeNao.isSelected()) {
                modUsuario.setSabe_ler("NAO");
            }

            if (jRadioButtonFrequentaSim.isSelected()) {
                modUsuario.setFequenta_escola("SIM");
            } else {
            }
            if (jRadioButtonFrequentaNao.isSelected()) {
                modUsuario.setFequenta_escola("NAO");
            }

            modUsuario.setEscolaridade(String.valueOf(jComboBoxEscolaridade.getSelectedItem()));

            if (jRadioButtonPossuiSim.isSelected()) {
                modUsuario.setPossui_ctps("SIM");
            } else {
            }
            if (jRadioButtonPossuiNao.isSelected()) {
                modUsuario.setPossui_ctps("NAO");
            }

            modUsuario.setCondicao_ocupacao(String.valueOf(jComboBoxCondicao.getSelectedItem()));

            modUsuario.setQualificacao_profissional(jTextFieldQualificao.getText().toUpperCase());
            modUsuario.setRenda_mensal(Double.parseDouble(jTextFieldRendaMensal.getText()));
            modUsuario.setId_usuario(id_usuario);

            modUsuario.setUltimo_registro(getDataSistema());

            ControleUsuario controlUsuario = new ControleUsuario();
            controlUsuario.AtualizarUsuario(modUsuario);

            desabilitaTexto();
            limpaTexto();

        } else {
            JOptionPane.showMessageDialog(null, "VERIFICAR OS CAMPOS!\n1 - NOME DO USUARIO NAO PODE SER NULO\n2 - A RENDA DEVE SER EM NUMERAL");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

