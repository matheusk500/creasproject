    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (jDateChooser1.getDate() != null && !jTextFieldUsuario.getText().trim().isEmpty() && jComboBoxTipoAtendimento.getSelectedIndex() != 0) {
            ModeloFamilia modFamilia = new ModeloFamilia();
            modFamilia.setId_usuario(id_usuario);

            modFamilia.setTipo_atendimento(String.valueOf(jComboBoxTipoAtendimento.getSelectedItem()));
            if(jCheckBox1.isSelected()){
                modFamilia.setSituacao_atendimento("PENDENTE");
            }else{
                modFamilia.setSituacao_atendimento("REALIZADO");
            }
            
            if (jDateChooser1.getDate() == null) {
                modFamilia.setData_atendimento(null);
            } else {
                java.util.Date pega = jDateChooser1.getDate();
                SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
                String data = formato.format(pega);
                modFamilia.setData_atendimento(data);
            }

            modFamilia.setUltimo_registro(getDataSistema());
            modFamilia.setDescricao_tendimento((jTextAreaDescricao.getText()+"\n\nENDERECO:\n"+jTextFieldEndereco.getText()+", "+jTextFieldBairro.getText()+", "+jTextFieldNumero.getText()).toUpperCase());
            ControleFamilia controlFamilia = new ControleFamilia();
            controlFamilia.InserirAtendimentoEspontaneo(modFamilia);
            
            jTableUsuarios.setEnabled(false);
            jTextAreaDescricao.setEnabled(false);
            jTextFieldBairro.setEnabled(false);
            jTextFieldEndereco.setEnabled(false);
            jTextFieldNumero.setEnabled(false);
            jTextFieldUsuario.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jDateChooser1.setEnabled(false);
            jComboBoxTipoAtendimento.setEnabled(false);
            jCheckBox1.setEnabled(false);
            
            jTextAreaDescricao.setText("");
            jTextFieldBairro.setText("");
            jTextFieldEndereco.setText("");
            jTextFieldNumero.setText("");
            jTextFieldUsuario.setText("");
            jComboBoxTipoAtendimento.setSelectedIndex(0);
            jDateChooser1.setDate(null);
            jCheckBox1.setSelected(false);

        } else {
            JOptionPane.showMessageDialog(null, "VERIFICAR OS CAMPOS!\n1 - A DATA DO ATENDIMENTO NAO PODE SER NULA\n2 - INFORME O TIPO DE ATENDIMENTO\n3 - INFORME O USUARIO ");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

