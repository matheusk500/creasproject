    public String getDataSistema() {
        Timestamp dataDeHoje = new Timestamp(System.currentTimeMillis());
        return "" + dataDeHoje + " - " + System.getProperty("login");
    }

    public void preencherUsuarioBairro(int id_usuario) {
        String sql = "select * from fastflowcreas.usuario where id_usuario =" + id_usuario;
        try {
            con.excutaSQL(sql);
            con.rs.first();
            jTextFieldBairro.setText(con.rs.getString("bairro"));
            jTextFieldEndereco.setText(con.rs.getString("endereco"));
            jTextFieldNumero.setText(con.rs.getString("numero"));

        } catch (SQLException ex) {
            Logger.getLogger(ControleFamilia.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void preencherTabelaUsuarios(String sql) throws SQLException {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"COD", "NOME", "CPF", "RG", "NIS"};
        con.excutaSQL(sql);
        try {
            con.rs.first();
            do {
                dados.add(new Object[]{con.rs.getInt("id_usuario"), con.rs.getString("nome_usuario"), con.rs.getString("cpf"), con.rs.getString("rg"), con.rs.getString("nis")});
            } while (con.rs.next());
        } catch (SQLException ex) {
            //JOptionPane.showMessageDialog(null, "USUARIO NAO ENCONTRADO");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableUsuarios.setModel(modelo);
        jTableUsuarios.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTableUsuarios.getColumnModel().getColumn(0).setResizable(true);
        jTableUsuarios.getColumnModel().getColumn(1).setPreferredWidth(350);
        jTableUsuarios.getColumnModel().getColumn(1).setResizable(true);
        jTableUsuarios.getColumnModel().getColumn(2).setPreferredWidth(120);
        jTableUsuarios.getColumnModel().getColumn(2).setResizable(true);
        jTableUsuarios.getColumnModel().getColumn(3).setPreferredWidth(120);
        jTableUsuarios.getColumnModel().getColumn(3).setResizable(true);
        jTableUsuarios.getColumnModel().getColumn(4).setPreferredWidth(135);
        jTableUsuarios.getColumnModel().getColumn(4).setResizable(true);
        jTableUsuarios.getTableHeader().setReorderingAllowed(false);
        jTableUsuarios.setAutoResizeMode(jTableUsuarios.AUTO_RESIZE_ALL_COLUMNS);
        jTableUsuarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

