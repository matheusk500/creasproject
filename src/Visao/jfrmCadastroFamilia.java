    private void jButtonInserirUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirUsuarioActionPerformed
        ModeloFamilia modFamilia = new ModeloFamilia();
        modFamilia.setId_familia(id_familia);
        modFamilia.setId_usuario(id_usuario);
        modFamilia.setParentesco(String.valueOf(jComboBoxParentesco.getSelectedItem()));
        if (verificaCamposFamilia() == 0) {
            JOptionPane.showMessageDialog(null, "INFORME O USUARIO A SER VINCULADO!");
        } else {
            ControleFamilia controlFamilia = new ControleFamilia();
            if (controlFamilia.InserirFamiliaUsuario(modFamilia) == 0) {
                jTextFieldUsuario1.setText("");
                jComboBoxParentesco.setSelectedIndex(0);
                jComboBoxParentesco.setEnabled(true);
                try {
                    preencherTabelaUsuarioVinculado("SELECT * FROM fastflowcreas.familia_usuario, fastflowcreas.usuario where fastflowcreas.familia_usuario.id_usuario =fastflowcreas.usuario.id_usuario and fastflowcreas.familia_usuario.id_familia = " + id_familia + " order by parentesco");
                } catch (SQLException ex) {
                    Logger.getLogger(jfrmCadastroFamilia.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                jTextFieldUsuario1.setText("");
            }
        }
    }//GEN-LAST:event_jButtonInserirUsuarioActionPerformed

