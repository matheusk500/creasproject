    private void jButtonNovaFamiliaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNovaFamiliaActionPerformed
        ModeloFamilia modFamilia = new ModeloFamilia();
        modFamilia.setEspecificidade("");
        ControleFamilia controlfFamilia = new ControleFamilia();
        controlfFamilia.InserirFamilia(modFamilia);
        jComboBoxParentesco.setSelectedIndex(1);
        try {
            con.conectar();
            con.excutaSQL("select * from fastflowcreas.familia");
            con.rs.last();
            id_familia = con.rs.getInt("id_familia");
            jLabelCod.setText("" + id_familia);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
            Logger.getLogger(jfrmCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        habilitaTexto();
        jButtonNovaFamilia.setEnabled(false);
    }//GEN-LAST:event_jButtonNovaFamiliaActionPerformed

